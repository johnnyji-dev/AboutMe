export function getInitialLang() {
  const saved = localStorage.getItem(STORAGE_KEY_LANG);
  if (saved === "ko" || saved === "en") return saved;
  const n = navigator.language || "";
  return n.toLowerCase().startsWith("ko") ? "ko" : "en";
}
